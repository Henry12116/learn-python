# loops let you repeat a block of code
# for example:
for i in range(5):
    print(i)

# without loops, we would do shit like this:
print(1)
print(2)
print(3)
print(4)
# ...

# D.R.Y. (Don't Repeat Yourself)
john = "John"
doe = "Doe"
for i in range(100):
    print(john, doe)


for thing in [1, 2, 3, 4]:
    print(thing)


for fuck in "abcdefghijklmnopqrstuvwxyz":
    print(fuck)
